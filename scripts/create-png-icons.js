#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

// Create minimal valid PNG files (transparent 1x1) as placeholders
// In production, replace with actual icons using sharp or ImageMagick

const minPNG = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4, 0x89, 0x00, 0x00, 0x00,
  0x0a, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9c, 0x63, 0x00, 0x01, 0x00, 0x00,
  0x05, 0x00, 0x01, 0x0d, 0x0a, 0x2d, 0xb4, 0x00, 0x00, 0x00, 0x00, 0x49,
  0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82
]);

const publicDir = path.join(__dirname, '..', 'public');
const pngs = [
  'icon-192x192.png',
  'icon-512x512.png',
  'badge-72x72.png'
];

pngs.forEach(png => {
  const pngPath = path.join(publicDir, png);
  fs.writeFileSync(pngPath, minPNG);
  console.log(`✓ Created placeholder ${png}`);
});

console.log('\n⚠️  Placeholder PNG files created for development.');
console.log('For production, replace with actual icons:');
